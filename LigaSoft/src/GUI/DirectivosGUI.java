     private void limpiar(){
       txtIdDirectivo.setText(null);
       txtNombreCompleto.setText(null);
       cbxSexo.setSelectedItem(null);
       txtEdad.setText(null);
       cbxPosicion.setSelectedItem(null);
       cbxEquipo.setSelectedItem(null);
    }
